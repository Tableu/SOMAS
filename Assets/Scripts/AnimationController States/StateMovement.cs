using System.Collections.Generic;
using Enemy;
using UnityEngine;

namespace AnimationController_States {
    public class StateMovement : StateMachineBehaviour
    {
        public string[] movementMethods;
        private List<object> list = new List<object>(); //Arguments for EnemyMovement Methods
        // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
        public override void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
        {
            list.Clear();
            list.Add(animator.gameObject);
        }

        // OnStateUpdate is called on each Update frame between OnStateEnter and OnStateExit callbacks
        public override void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
        {
            foreach (var t in movementMethods)
            {
                //Call all methods attached to the state
                EnemyMovement.InvokeMethod(t,list);
            }
        }

